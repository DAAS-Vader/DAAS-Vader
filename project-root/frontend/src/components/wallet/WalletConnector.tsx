"use client"

import React, { useState, useEffect } from 'react'
import { motion, AnimatePresence } from 'framer-motion'
import {
  Wallet,
  CheckCircle,
  AlertCircle,
  Copy,
  ExternalLink,
  LogOut,
  RefreshCw,
  Shield
} from 'lucide-react'
import { Button } from '@/components/ui/button'
import { Card } from '@/components/ui/card'
import { Badge } from '@/components/ui/badge'
import { WalletInfo } from '@/types'
import { useWallet } from '@suiet/wallet-kit'

interface WalletConnectorProps {
  onConnect: (wallet: WalletInfo) => void
  onDisconnect: () => void
  currentWallet?: WalletInfo | null
}

interface SupportedWallet {
  id: 'Suiet' | 'Slush'
  name: string
  icon: string
  description: string
  downloadUrl: string
}

const WalletConnector: React.FC<WalletConnectorProps> = ({
  onConnect,
  onDisconnect,
  currentWallet
}) => {
  const [isConnecting, setIsConnecting] = useState(false)
  const [connectionError, setConnectionError] = useState<string | null>(null)
  const [copiedAddress, setCopiedAddress] = useState(false)
  const [isDisconnecting, setIsDisconnecting] = useState(false)

  // Use Suiet wallet hook for real wallet integration
  const {
    wallet,
    connected,
    connecting,
    select,
    disconnect,
    signPersonalMessage,
    account
  } = useWallet()

  // Generate authentication signature for API calls
  const generateAuthSignature = async (walletAddress: string): Promise<string | null> => {
    try {
      const timestamp = Date.now()
      const message = `DaaS Authentication\nTimestamp: ${timestamp}\nWallet: ${walletAddress}`

      // Try to sign message for authentication
      let signature = null
      try {
        const signatureResult = await signPersonalMessage({
          message: new TextEncoder().encode(message)
        })
        signature = signatureResult.signature
      } catch (signError) {
        console.warn('Failed to sign message, using simplified auth:', signError)
        signature = 'unsigned' // Fallback for development
      }

      const authData = {
        walletAddress,
        signature,
        message,
        timestamp
      }

      return JSON.stringify(authData)
    } catch (error) {
      console.error('Failed to generate auth signature:', error)
      return null // Don't fail connection if signature fails
    }
  }

  const supportedWallets: SupportedWallet[] = [
    {
      id: 'Suiet',
      name: 'Suiet Wallet',
      icon: 'üíé',
      description: 'Sui Ï†ÑÏö© ÏßÄÍ∞ë',
      downloadUrl: 'https://chrome.google.com/webstore/detail/suiet-sui-wallet'
    },
    {
      id: 'Slush',
      name: 'Slush Wallet',
      icon: 'üåä',
      description: 'Sui ÏÉùÌÉúÍ≥Ñ ÏßÄÍ∞ë',
      downloadUrl: 'https://chrome.google.com/webstore/detail/slush-wallet'
    }
  ]

  const connectWallet = async (walletType: 'Suiet' | 'Slush') => {
    console.log('Attempting to connect wallet:', walletType)
    setIsConnecting(true)
    setConnectionError(null)

    try {
      // Connect to the actual wallet - Suiet wallet-kit API
      console.log('Calling select()...', walletType)
      await select(walletType)
      console.log('Select() successful')
    } catch (error) {
      console.error('Wallet connection failed:', error)
      console.error('Error details:', error)
      setConnectionError(`ÏßÄÍ∞ë Ïó∞Í≤∞Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§. ${error?.message || error?.toString() || ''} Î∏åÎùºÏö∞Ï†ÄÏóê ÏßÄÍ∞ë ÌôïÏû•ÌîÑÎ°úÍ∑∏Îû®Ïù¥ ÏÑ§ÏπòÎêòÏñ¥ ÏûàÎäîÏßÄ ÌôïÏù∏Ìï¥Ï£ºÏÑ∏Ïöî.`)
      setIsConnecting(false)
    }
  }

  // Effect to handle wallet connection state changes
  useEffect(() => {
    const handleWalletConnection = async () => {
      // Don't handle connection if we're in the process of disconnecting
      if (isDisconnecting) {
        return
      }

      if (connected && account && account.address) {
        try {
          // Get wallet balance - simplified for now
          let suiBalance = 0
          // Note: @suiet/wallet-kit doesn't have getBalance, we'll use a placeholder
          suiBalance = 0 // Default balance - would need to fetch from RPC

          // Generate auth signature for API calls
          const authSignature = await generateAuthSignature(account.address)

          const walletInfo: WalletInfo = {
            connected: true,
            address: account.address,
            balance: suiBalance,
            provider: wallet?.name?.includes('Slush') ? 'slush' : 'suiet',
            authSignature
          }

          onConnect(walletInfo)
          setIsConnecting(false)
        } catch (error) {
          console.error('Failed to initialize wallet:', error)
          setConnectionError('ÏßÄÍ∞ë Ï¥àÍ∏∞ÌôîÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.')
          setIsConnecting(false)
        }
      } else if (!connected && !connecting && !isDisconnecting) {
        setIsConnecting(false)
      }
    }

    handleWalletConnection()
  }, [connected, account, connecting, isDisconnecting])

  const disconnectWallet = async () => {
    setIsDisconnecting(true)
    try {
      // Clear local storage to prevent auto-reconnection
      localStorage.removeItem('daas-wallet')

      await disconnect()
      onDisconnect()
      setConnectionError(null)

      // Keep disconnecting state for a moment to prevent immediate reconnection
      setTimeout(() => {
        setIsDisconnecting(false)
      }, 1000)
    } catch (error) {
      console.error('Failed to disconnect wallet:', error)
      setConnectionError('ÏßÄÍ∞ë Ïó∞Í≤∞ Ìï¥Ï†úÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.')
      setIsDisconnecting(false)
    }
  }

  const copyAddress = async () => {
    if (currentWallet?.address) {
      await navigator.clipboard.writeText(currentWallet.address)
      setCopiedAddress(true)
      setTimeout(() => setCopiedAddress(false), 2000)
    }
  }

  const refreshBalance = async () => {
    if (currentWallet) {
      // Ïã§Ï†úÎ°úÎäî ÏßÄÍ∞ë APIÎ•º Ìò∏Ï∂úÌïòÏó¨ ÏûîÏï°ÏùÑ ÏÉàÎ°úÍ≥†Ïπ®
      // Mock Íµ¨ÌòÑ
      const updatedWallet: WalletInfo = {
        ...currentWallet,
        balance: Math.random() * 1000
      }
      onConnect(updatedWallet)
    }
  }

  const formatAddress = (address: string) => {
    return `${address.slice(0, 6)}...${address.slice(-4)}`
  }

  const formatBalance = (balance: number) => {
    return balance.toLocaleString('ko-KR', {
      minimumFractionDigits: 2,
      maximumFractionDigits: 2
    })
  }

  const getWalletIcon = (provider: string) => {
    const providerMap: Record<string, string> = {
      'suiet': 'Suiet',
      'slush': 'Slush'
    }
    const walletId = providerMap[provider] || provider
    const wallet = supportedWallets.find(w => w.id === walletId)
    return wallet?.icon || 'üíº'
  }

  if (currentWallet?.connected) {
    return (
      <motion.div
        initial={{ opacity: 0, y: -20 }}
        animate={{ opacity: 1, y: 0 }}
        className="space-y-4"
      >
        <Card className="p-4">
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-3">
              <div className="flex items-center gap-2">
                <span className="text-2xl">{getWalletIcon(currentWallet.provider)}</span>
                <div>
                  <h3 className="font-semibold">
                    {currentWallet.provider === 'suiet' ? 'Suiet Wallet' : 'Slush Wallet'}
                  </h3>
                  <div className="flex items-center gap-2">
                    <span className="text-sm text-muted-foreground">
                      {formatAddress(currentWallet.address)}
                    </span>
                    <Button
                      variant="ghost"
                      size="icon"
                      className="w-4 h-4 p-0"
                      onClick={copyAddress}
                    >
                      {copiedAddress ? (
                        <CheckCircle className="w-3 h-3 text-green-500" />
                      ) : (
                        <Copy className="w-3 h-3" />
                      )}
                    </Button>
                  </div>
                </div>
              </div>

              <Badge variant="secondary" className="bg-green-100 text-green-700 border-green-200">
                <CheckCircle className="w-3 h-3 mr-1" />
                Ïó∞Í≤∞Îê®
              </Badge>
            </div>

            <div className="flex items-center gap-2">
              <Button
                variant="ghost"
                size="icon"
                onClick={refreshBalance}
                className="w-8 h-8"
              >
                <RefreshCw className="w-4 h-4" />
              </Button>
              <Button
                variant="ghost"
                size="icon"
                onClick={disconnectWallet}
                className="w-8 h-8 text-red-500 hover:text-red-600"
              >
                <LogOut className="w-4 h-4" />
              </Button>
            </div>
          </div>

          <div className="mt-4 pt-4 border-t">
            <div className="flex items-center justify-between">
              <span className="text-sm text-muted-foreground">SUI ÏûîÏï°</span>
              <div className="text-right">
                <span className="font-semibold text-lg">
                  {formatBalance(currentWallet.balance)} SUI
                </span>
                <p className="text-xs text-muted-foreground">
                  ‚âà ${(currentWallet.balance * 2.5).toFixed(2)} USD
                </p>
              </div>
            </div>
          </div>
        </Card>

        {/* Account Info */}
        <Card className="p-4">
          <div className="flex items-center gap-2 mb-3">
            <Shield className="w-4 h-4 text-primary" />
            <span className="font-medium">Í≥ÑÏ†ï Ï†ïÎ≥¥</span>
          </div>

          <div className="space-y-3 text-sm">
            <div className="flex justify-between">
              <span className="text-muted-foreground">Í≥ÑÏ†ï Îì±Í∏â</span>
              <Badge variant="outline">Starter</Badge>
            </div>
            <div className="flex justify-between">
              <span className="text-muted-foreground">Ïä§ÌÖåÏù¥ÌÇπ Í∞ÄÎä•</span>
              <span className="text-green-600">‚úì Ïòà</span>
            </div>
            <div className="flex justify-between">
              <span className="text-muted-foreground">ÎÑ§Ìä∏ÏõåÌÅ¨</span>
              <span>Sui Mainnet</span>
            </div>
          </div>
        </Card>
      </motion.div>
    )
  }

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      className="space-y-6"
    >
      {/* Header */}
      <div className="text-center space-y-2">
        <div className="w-16 h-16 mx-auto rounded-full bg-primary/10 flex items-center justify-center">
          <Wallet className="w-8 h-8 text-primary" />
        </div>
        <h2 className="text-2xl font-bold">ÏßÄÍ∞ë Ïó∞Í≤∞</h2>
        <p className="text-muted-foreground">
          DaaS ÌîåÎû´ÌèºÏùÑ ÏÇ¨Ïö©ÌïòÎ†§Î©¥ Sui ÏßÄÍ∞ëÏùÑ Ïó∞Í≤∞Ìï¥Ï£ºÏÑ∏Ïöî
        </p>
      </div>

      {/* Error Message */}
      <AnimatePresence>
        {connectionError && (
          <motion.div
            initial={{ opacity: 0, y: -10 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: -10 }}
            className="p-4 bg-red-50 border border-red-200 rounded-lg"
          >
            <div className="flex items-center gap-2">
              <AlertCircle className="w-4 h-4 text-red-500" />
              <span className="text-sm text-red-700">{connectionError}</span>
            </div>
          </motion.div>
        )}
      </AnimatePresence>

      {/* Wallet Options */}
      <div className="space-y-3">
        {supportedWallets.map(wallet => (
          <motion.div
            key={wallet.id}
            whileHover={{ scale: 1.02 }}
            whileTap={{ scale: 0.98 }}
          >
            <Card
              className={`p-4 cursor-pointer transition-all hover:shadow-md ${
                isConnecting ? 'opacity-50 pointer-events-none' : ''
              }`}
              onClick={() => connectWallet(wallet.id)}
            >
              <div className="flex items-center justify-between">
                <div className="flex items-center gap-3">
                  <span className="text-2xl">{wallet.icon}</span>
                  <div>
                    <h3 className="font-semibold">{wallet.name}</h3>
                    <p className="text-sm text-muted-foreground">
                      {wallet.description}
                    </p>
                  </div>
                </div>

                <div className="flex items-center gap-2">
                  <a
                    href={wallet.downloadUrl}
                    target="_blank"
                    rel="noopener noreferrer"
                    onClick={(e) => e.stopPropagation()}
                    className="text-primary hover:text-primary/80"
                  >
                    <ExternalLink className="w-4 h-4" />
                  </a>

                  {isConnecting ? (
                    <motion.div
                      animate={{ rotate: 360 }}
                      transition={{ duration: 1, repeat: Infinity, ease: "linear" }}
                    >
                      <RefreshCw className="w-4 h-4" />
                    </motion.div>
                  ) : (
                    <Button variant="ghost" size="sm">
                      Ïó∞Í≤∞
                    </Button>
                  )}
                </div>
              </div>
            </Card>
          </motion.div>
        ))}
      </div>

      {/* Security Notice */}
      <Card className="p-4 bg-blue-50 border-blue-200">
        <div className="flex items-start gap-3">
          <Shield className="w-5 h-5 text-blue-600 mt-0.5" />
          <div className="space-y-1">
            <h4 className="font-medium text-blue-900">Î≥¥Ïïà ÏïàÎÇ¥</h4>
            <ul className="text-sm text-blue-700 space-y-1">
              <li>‚Ä¢ ÏßÄÍ∞ë Ïó∞Í≤∞ Ïãú Í∞úÏù∏ÌÇ§Îäî Ï†àÎåÄ Í≥µÏú†ÎêòÏßÄ ÏïäÏäµÎãàÎã§</li>
              <li>‚Ä¢ Î™®Îì† Ìä∏ÎûúÏû≠ÏÖòÏùÄ ÏÇ¨Ïö©ÏûêÏùò Î™ÖÏãúÏ†Å ÏäπÏù∏Ïù¥ ÌïÑÏöîÌï©ÎãàÎã§</li>
              <li>‚Ä¢ Î∏åÎùºÏö∞Ï†ÄÎ•º Îã´ÏúºÎ©¥ Ïó∞Í≤∞Ïù¥ ÏûêÎèôÏúºÎ°ú Ìï¥Ï†úÎê©ÎãàÎã§</li>
            </ul>
          </div>
        </div>
      </Card>

      {/* Help Links */}
      <div className="text-center space-y-2">
        <p className="text-sm text-muted-foreground">
          ÏßÄÍ∞ëÏù¥ ÏóÜÏúºÏã†Í∞ÄÏöî?
        </p>
        <div className="flex justify-center gap-2">
          {supportedWallets.map(wallet => (
            <a
              key={wallet.id}
              href={wallet.downloadUrl}
              target="_blank"
              rel="noopener noreferrer"
              className="text-primary hover:text-primary/80 text-sm underline"
            >
              {wallet.name} ÏÑ§Ïπò
            </a>
          ))}
        </div>
      </div>
    </motion.div>
  )
}

